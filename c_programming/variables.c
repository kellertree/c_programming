#include <stdio.h> // Include the necessary library for printf

int main()
{                       // The program must start in the main function
    int age = 25;       // Declare and initialize an integer variable
    float height = 5.9; // Declare and initialize a floating-point variable
    char initial = 'A'; // Declare and initialize a character variable

    // Use printf to display the values
    printf("Age: %d, Height: %.1f, Initial: %c\n", age, height, initial);

    return 0; // Return 0 to indicate successful execution
}
